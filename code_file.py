prob4b += emiss4b
prob4b += cost4b <= min_cost * 1.01
prob4b += pulp.lpSum([x4b[(i,s)] for i in sites for s in sources]) == demand
for i in sites:
    prob4b += pulp.lpSum([x4b[(i,s)] for s in sources]) <= max_cap[i]
